#!/usr/bin/node

const BaseSquare = require('./5-square.js');

class Square extends BaseSquare {
  // ==========================================================================
  // ==Constructor=============================================================
  // ==========================================================================

  // ==========================================================================
  // ==Methods=================================================================
  // ==========================================================================

  // Prints in the STDOUT the given character, if no character is defined, it
  // defaults to 'X'

  charPrint (c) {
    if (!c) {
      c = 'X';
    }
    let i = this.width;
    while (i > 0) {
      console.log(c.repeat(this.width));
      i--;
    }
  }
  // ==========================================================================
  // ==END=====================================================================
  // ==========================================================================
}
module.exports = Square;
